arrow3d(c(0, 0, 1), c(0.43, 0.43, 1), s = 1/7, type = "rotation", col = "black")
text3d(x = 0, y = 0.3, z = 0.8, texts = "a3", cex = 1.5, color = "black")
arrow3d(c(0, 0, 1), c(0, 0.42, 0.56), s = 1/7, type = "rotation", col = "black")
